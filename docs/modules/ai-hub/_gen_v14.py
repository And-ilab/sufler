def load_comments(path: Path) -> list[tuple[str, str, str, str]]:
    rows = []
    if not path.exists():
        return rows
    for line in path.read_text(encoding="utf-8").splitlines():
        m = re.match(r"\[(\d+)\] ([^(]+) \(([^)]+)\): (.+)", line)
        if m:
            rows.append((m.group(1), m.group(2).strip(), m.group(3), m.group(4).strip()))
    return rows


