def expand_uc_table(block: str) -> str:
    lines = block.strip().split("\n")
    if len(lines) < 3:
        return block
    header = lines[0]
    if "Реализация" in header:
        return block

    if "| UC " in header and "Наименование" in header:
        new_header = "| UC | Наименование | Реализация | Пункт в ТТ | Статус |"
        new_sep = "| --- | --- | --- | --- | --- |"
    elif "| ID" in header and "Сценарий" in header:
        new_header = "| ID | Наименование | Реализация | Пункт в ТТ | Статус |"
        new_sep = "| --- | --- | --- | --- | --- |"
    elif "| ID" in header and "Описание" in header and re.search(r"\| UC-", block):
        new_header = re.sub(
            r"\| Описание \|",
            "| Наименование | Реализация |",
            header.replace("Описание", "Наименование", 1) if "Наименование" not in header else header,
        )
        if "Наименование" not in new_header:
            new_header = header.replace("Описание", "Наименование | Реализация")
        if "Реализация" not in new_header:
            new_header = re.sub(r"\| Наименование \|", "| Наименование | Реализация |", header.replace("Описание", "Наименование"))
        new_sep = "| " + " | ".join(["---"] * (new_header.count("|") - 1)) + " |"
    else:
        return block

    out = [new_header, new_sep]
    for line in lines[2:]:
        if not line.strip().startswith("|"):
            continue
        cells = [c.strip() for c in line.split("|")[1:-1]]
        if len(cells) < 4:
            out.append(line)
            continue
        uid, name = cells[0], cells[1]
        tt, status = cells[2], cells[3]
        real = R.get(uid, f"См. [I.1.2](#i12-формат-описания-функции-v13): вход → RAG/LLM → UI.")
        out.append(f"| {uid} | {name} | {real} | {tt} | {status} |")
    return "\n".join(out)


def expand_fr_table(block: str) -> str:
    lines = block.strip().split("\n")
    if len(lines) < 3:
        return block
    header = lines[0]
    if "Реализация" in header:
        return block
    if "Описание" not in header:
        return block
    if not re.search(r"\| FR-", block):
        return block

    new_header = "| ID | Описание | Реализация | Пункт в ТТ | Статус |"
    new_sep = "| --- | --- | --- | --- | --- |"
    out = [new_header, new_sep]

    for line in lines[2:]:
        if not line.strip().startswith("|"):
            continue
        cells = [c.strip() for c in line.split("|")[1:-1]]
        if len(cells) < 4:
            out.append(line)
            continue
        uid, desc = cells[0], cells[1]
        if not uid.startswith("FR-"):
            out.append(line)
            continue
        tt, status = cells[2], cells[3]
        real = get_fr_impl(uid, desc, R)
        out.append(f"| {uid} | {desc} | {real} | {tt} | {status} |")
    return "\n".join(out)


