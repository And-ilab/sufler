def normalize_title(title: str) -> str:
    return re.sub(r"\s+", " ", title.strip())


def section_key(title: str) -> str | None:
    t = normalize_title(title)
    if t in INTROS:
        return t
    return None


BODY_MARKER = "# Часть 0. Реквизиты документа"


def strip_legacy_intro(key: str, text: str) -> str:
    """Remove legacy one-line [Прил.1 …] and duplicate intro lines already in INTROS."""
    if not text:
        return text
    intro = INTROS.get(key, "")
    intro_lines = {
        ln.strip().lstrip("*").rstrip("*")
        for ln in intro.splitlines()
        if ln.strip() and not ln.strip().startswith("**Целевой")
    }
    lines = text.splitlines(keepends=True)
    while lines:
        first = lines[0].strip()
        if not first:
            lines = lines[1:]
            continue
        bare = first.lstrip("*").rstrip("*")
        if bare.startswith("[Прил.1") or bare.startswith("[ГОСТ") or bare.startswith("[I.1.2"):
            lines = lines[1:]
            continue
        if bare in intro_lines or any(
            bare.startswith(p[:50]) for p in intro_lines if len(p) > 40
        ):
            lines = lines[1:]
            continue
        break
    return "".join(lines).lstrip("\n")


def already_enhanced(key: str, block_after_heading: str) -> bool:
    head = block_after_heading[:2500]
    if "**Целевой эффект:**" in head:
        return True
    if "Здесь настраиваются:" in head:
        return True
    intro = INTROS.get(key, "")
    if key.startswith("Часть"):
        for line in intro.splitlines():
            s = line.strip()
            if s and not re.match(r"^\*?\*?\[Прил", s):
                return s[:80] in head
        return False
    if " обеспечивает:" in head and re.search(r"^-\s+", head, re.MULTILINE):
        return True
    # minor/config: full intro block already present
    sig = intro.strip()[:180]
    if sig and sig in head[: len(sig) + 80]:
        return True
    return False


def process(content: str) -> tuple[str, list[str]]:
    enhanced: list[str] = []
    body_start = content.find(BODY_MARKER)
    if body_start < 0:
        raise SystemExit(f"Body marker not found: {BODY_MARKER!r}")

    prefix = content[:body_start]
    body = content[body_start:]
    lines = body.splitlines(keepends=True)
    out: list[str] = []
    i = 0
    while i < len(lines):
        line = lines[i]
        m = HEADING_RE.match(line.rstrip("\n"))
        if not m:
            out.append(line)
            i += 1
            continue

        title = normalize_title(m.group("title"))
        key = section_key(title)
        level = len(m.group("level"))
        out.append(line)
        i += 1

        j = i
        while j < len(lines):
            nm = HEADING_RE.match(lines[j].rstrip("\n"))
            if nm and len(nm.group("level")) <= level:
                break
            j += 1
        following = "".join(lines[i:j])

        if key and key in INTROS and not already_enhanced(key, following):
            out.append(INTROS[key])
            out.append(strip_legacy_intro(key, following))
            enhanced.append(key)
            i = j
            continue

        # Non-enhanced headings: content lines are copied on subsequent iterations.

    return prefix + "".join(out), enhanced


def fix_toc(content: str) -> str:
    """Remove misplaced II.7.4 body from TOC."""
    marker = "\n##\n### II.7.4. Нагрузочное тестирование КЦ"
    if marker not in content:
        return content
    start = content.index(marker)
    m = re.search(
        r"\n#{1,3} Часть III\. Модуль ИИ-ассистент[^\n]*",
        content[start:],
    )
    if not m:
        return content
    match_end = start + m.end()
    return (
        content[:start]
        + "\n- [II.7.4 Нагрузочное тестирование КЦ](#ii74-нагрузочное-тестирование-кц)\n\n"
        + "### Часть III. Модуль ИИ-ассистент (§2.2.3, §5)"
        + content[match_end:]
    )


def main() -> None:
    import sys

    text = DOC.read_text(encoding="utf-8")
    text = fix_toc(text)
    new_text, enhanced = process(text)
    DOC.write_text(new_text, encoding="utf-8")
    log = DOC.with_name("_add_section_intros.log")
    lines_out = [f"Enhanced {len(enhanced)} sections", f"Lines: {new_text.count(chr(10))}"]
    for k in enhanced:
        first = INTROS[k].strip().split("\n")[0][:80]
        lines_out.append(f"  - {k}: {first}...")
    log.write_text("\n".join(lines_out), encoding="utf-8")
    sys.stdout.reconfigure(encoding="utf-8")  # type: ignore[attr-defined]
    print("\n".join(lines_out))


if __name__ == "__main__":
    main()
