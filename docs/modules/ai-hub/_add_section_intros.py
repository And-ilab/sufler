# heading regex: ## I.1. ... {#anchor} or # Часть I. ...
HEADING_RE = re.compile(
    r"^(?P<level>#{1,3})\s+(?P<title>.+?)(?:\s+\{#(?P<anchor>[^}]+)\})?\s*$",
    re.MULTILINE,
)

# section_id -> intro block (inserted after heading; part-level may prepend before [Прил.1)
INTROS: dict[str, str] = {}

def P(text: str) -> str:
    return text.strip() + "\n\n"

def part(key: str, para: str, pripl: str | None = None) -> None:
    block = P(para)
    if pripl:
        block += P(pripl)
    INTROS[key] = block.rstrip() + "\n\n"

def major(
    key: str,
    pripl: str,
    intro: str,
    bullets_or_effect: list[str] | str,
    effect: str | None = None,
) -> None:
    if effect is None:
        bullets: list[str] = []
        effect_str = str(bullets_or_effect)
    else:
        bullets = list(bullets_or_effect)  # type: ignore[arg-type]
        effect_str = effect
    lines = [f"**{pripl}**", "", intro.rstrip(), ""]
    if bullets:
        lines.extend(f"- {b}" for b in bullets)
        lines.append("")
    lines.append(f"**Целевой эффект:** {effect_str.rstrip()}")
    INTROS[key] = "\n".join(lines) + "\n\n"

def minor(key: str, pripl: str | None, body: str, effect: str | None = None) -> None:
    lines: list[str] = []
    if pripl:
        lines.extend([f"**{pripl}**", ""])
    lines.append(body.rstrip())
    if effect:
        lines.extend(["", f"**Целевой эффект:** {effect.rstrip()}"])
    INTROS[key] = "\n".join(lines) + "\n\n"

def config(key: str, pripl: str, intro: str, bullets: list[str], footer: str) -> None:
    lines = [f"**{pripl}**", "", intro.rstrip(), ""]
    lines.extend(f"- {b}" for b in bullets)
    lines.extend(["", footer.rstrip()])
    INTROS[key] = "\n".join(lines) + "\n\n"


