def apply_section(
    wrap: Callable[..., None],
    prefix: str,
    data: dict[str, tuple[str, str, str, str]],
) -> None:
    for num, (entry, under, out, accept) in data.items():
        wrap(f"{prefix}-{num}", entry=entry, under=under, out=out, accept=accept)


