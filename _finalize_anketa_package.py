def cleanup() -> None:
    # Remove duplicate Latin-ch folder
    for name in os.listdir(BASE):
        if name.startswith("\u0430\u043d\u043a\u0435\u0442\u0430") and "ch" in name and "\u0447\u0438\u0432\u043e\u0441\u0442\u0438" not in name:
            bad = os.path.join(BASE, name)
            shutil.rmtree(bad, ignore_errors=True)
            print("removed duplicate folder", name)

    # Remove root originals (copies in incoming/)
    incoming_names = {"anketa-zapolnennaya.pdf", "zaklyuchenie-bank.pdf", "perepiska.odt"}
    for fn in os.listdir(ROOT):
        if fn.endswith(".pdf") or fn.endswith(".odt"):
            if fn not in incoming_names:
                # keep if not duplicated in incoming
                pass
            src = os.path.join(ROOT, fn)
            if os.path.isfile(src):
                os.remove(src)
                print("removed root duplicate", fn)

    for junk in ["_open_paths.txt"]:
        p = os.path.join(ROOT, junk)
        if os.path.isfile(p):
            os.remove(p)
            print("removed", junk)


def export_docx() -> None:
    ps1 = os.path.join(ROOT, "export-to-word.ps1")
    r = subprocess.run(
        ["powershell", "-NoProfile", "-ExecutionPolicy", "Bypass", "-File", ps1],
        cwd=ROOT,
        capture_output=True,
        text=True,
        encoding="utf-8",
        errors="replace",
    )
    print(r.stdout)
    if r.returncode != 0:
        print(r.stderr, file=sys.stderr)
        sys.exit(r.returncode)


